    private void jBMedicineInputInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBMedicineInputInsertActionPerformed
        // TODO add your handling code here:
        if(valid.isNumber(jTProductQuantity.getText()) && valid.isNumber(jTProductPrice.getText()) && !valid.containsMedicine(new Medicine(jTProductName.getText(), Integer.parseInt(jTProductQuantity.getText()), CompanyChioce.getSelectedItem(), Integer.parseInt(jTProductPrice.getText()), ProductChoice.getSelectedItem())))
        {
            dbm.insertMedicine(new Medicine(jTProductName.getText(), Integer.parseInt(jTProductQuantity.getText()), CompanyChioce.getSelectedItem(), Integer.parseInt(jTProductPrice.getText()), ProductChoice.getSelectedItem()));
            Medicine m=dbm.searchMedicine(jTProductName.getText());
            deleteTableModel.addRow(new Object[]{Integer.toString(m.getId()), m.getName(), m.getCompany(), m.getType(), Integer.toString(m.getQuantity()), Integer.toString(m.getPrice())});
            tblDeleteTable.setModel(deleteTableModel);
        }
        
    }//GEN-LAST:event_jBMedicineInputInsertActionPerformed

