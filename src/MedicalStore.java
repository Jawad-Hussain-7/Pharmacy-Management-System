    private void jBUpdateMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBUpdateMedicineActionPerformed
        // TODO add your handling code here:
        
        setScreenSize(UpdateMedicinePanel);
        MedicinePanel.dispose();
        initUpdateTable();
        ArrayList <Medicine> list=dbm.getAllMedicines();
        for(Medicine m : list)
        {
            updateTableModel.addRow(new Object[]{m.getId(), m.getName(), m.getCompany(), m.getType(), Integer.toString(m.getQuantity()), Integer.toString(m.getPrice())});
        }
        tblUpdateTable.setModel(updateTableModel);
    }//GEN-LAST:event_jBUpdateMedicineActionPerformed

    private void jBDeleteMedicineActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBDeleteMedicineActionPerformed
        try {
            // TODO add your handling code here:
            int row=tblDeleteTable.getSelectedRow();
            if(row!=-1)
            {
                Medicine m=items.getItem(row);
                dbm.deleteMedicine(m.getId());
                deleteTableModel.removeRow(row);
                tblDeleteTable.setModel(deleteTableModel);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MedicalStore.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jBDeleteMedicineActionPerformed

